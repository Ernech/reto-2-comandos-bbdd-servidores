package models.googlescholar;

public class SearchParameters {
    private String engine;
    private String q;
    private String hl;
    private int start;
    private String num;

    public String getEngine() {
        return engine;
    }

    public String getQ() {
        return q;
    }

    public String getHl() {
        return hl;
    }

    public int getStart() {
        return start;
    }

    public String getNum() {
        return num;
    }
}
